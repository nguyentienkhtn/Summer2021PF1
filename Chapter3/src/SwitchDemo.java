public class SwitchDemo {
    public static void main(String[] args) {
        int a = 8;
        switch (a){
            case 0:
                System.out.println("a");
                break;
            case 1:
                System.out.println("b");
                break;
            case 3:
                System.out.println("c");

            default:
                System.out.println("hello");
        }

    }
}
