/**
   This program demonstrates the while loop.
*/

public class WhileLoop
{
   public static void main(String [] args)
   {
      int number = 10;

      while (number <= 5)
      {
         System.out.println("Hello");
         number++;
         System.out.println(number);
      }

      System.out.println("That's all!");
   }
}
